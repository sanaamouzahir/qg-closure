def diagnostics(omega_np):
    """RMS, total enstrophy, total energy (placeholders; energy needs psi).
    For this script we use simpler L2 norm + enstrophy from omega only."""
    Z = 0.5 * float(np.mean(omega_np ** 2))     # mean enstrophy
    rms = float(np.sqrt(np.mean(omega_np ** 2)))
    return rms, Z


