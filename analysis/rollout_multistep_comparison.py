"""
rollout_multistep_comparison.py
================================

Multi-step rollout comparison of three trajectories starting from the same IC:

    (i)  fine reference (truth):   M * K AB2CN2 steps of size h_fine = Delta_T / K
    (ii) coarse no closure:        M     AB2CN2 steps of size Delta_T
    (iii)coarse with closure:      M     AB2CN2 steps of size Delta_T,
                                   each augmented by e_anal + e_NN_pred

Why this figure exists
----------------------
At validated (Delta_T, K) = (1e-3, 100), the per-step truncation error is
~ Delta_T^3 * |E| ~ 1e-9 -- invisible against |omega| ~ O(1) on any
field-space colorbar. The closure's job is to extend usable Delta_T much
higher than the bare scheme allows.  Error accumulation over M coarse
steps is what makes the closure benefit visible:

    bare:    error ~ M * C * Delta_T^3
    closure: error ~ M * C * Delta_T^3 * eps_NN     (eps_NN ~ 0.19)

so over an extended horizon T_rollout = M * Delta_T the closure trajectory
stays close to truth while the bare-coarse one drifts.

This script also fixes the broken spectra in visualize_rollout_correction.py:
the previous version *averaged* |omega_hat|^2 within each k-annulus, which
divides out the k-density-of-states factor and flattens the canonical
k^{-3} forward-enstrophy-cascade slope.  Here we sum within each annulus,
divide by bin width dk, and use physical wavenumbers (correct for any
L_x, L_y, not just 2*pi).

Outputs
-------
<out-dir>/rollout_multistep_<ic_tag>.png         four-row figure
<out-dir>/rollout_multistep_<ic_tag>.npz         all rollout arrays
<out-dir>/rollout_multistep_<ic_tag>.json        run metadata

Usage
-----
  python rollout_multistep_comparison.py \
      --run-dir  <training_run_dir> \
      --root-dir <dataset_root_dir> \
      --n-steps  500 \
      --ic-index 100 \
      --split    val \
      --out-dir  <run-dir>
"""

