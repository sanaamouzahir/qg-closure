def load_model(run_dir, n_in, hidden, kernel, base_channels, depth, model_name,
               device='cpu'):
    if model_name in ('bilinear_closure', 'bilin', 'fixd_v2'):
        from model_fixD import build_model
        model = build_model(model_name, in_channels=n_in,
                            hidden=hidden, kernel=kernel)
    else:
        from model import build_model
        if model_name == 'unet':
            model = build_model('unet', in_channels=n_in,
                                base_channels=base_channels, kernel=kernel)
        else:
            model = build_model('cnn', in_channels=n_in,
                                hidden_channels=hidden, depth=depth,
                                kernel=kernel)
    ckpt_path = run_dir / 'model_best.pt'
    if not ckpt_path.exists():
        ckpt_path = run_dir / 'model_last.pt'
    if not ckpt_path.exists():
        raise FileNotFoundError(f"no model_best.pt or model_last.pt in {run_dir}")
    state = torch.load(ckpt_path, map_location=device, weights_only=False)
    if 'model_state' in state:
        model.load_state_dict(state['model_state'])
    elif 'state_dict' in state:
        model.load_state_dict(state['state_dict'])
    else:
        model.load_state_dict(state)
    model.to(device).eval()
    print(f"  loaded checkpoint: {ckpt_path}")
    return model


def assemble_inputs(input_fields, omega_stack, psi_stack, derivative,
                    L_hat, F_phys, dtype, device):
    """Build the C_in-channel input tensor (1, C, Ny, Nx) from the current
    rollout state.

    omega_stack: list-of-tensors [omega_0, omega_m1, omega_m2, ...]
                 (most recent first).  Each (1, Ny, Nx) torch tensor.
    psi_stack:   matching list of psi_phys tensors.

    Only the input fields actually used by the loaded model are computed.
    The bilinear v2 net uses just the 6 channels:
        omega_0 omega_m1 omega_m2 psi_0 psi_m1 psi_m2
    so the heavy analytical ones (L^k omega, N_0, ...) are only built on demand.
    """
    from qg.solver.opt.basis import to_spectral, to_physical
    chans = []

    # Trivial channel: pulls from one of the stacks. Adds zero-pad if needed.
    def grab_omega(k):
        if k < len(omega_stack):
            return omega_stack[k]
        return torch.zeros_like(omega_stack[0])

    def grab_psi(k):
        if k < len(psi_stack):
            return psi_stack[k]
        return torch.zeros_like(psi_stack[0])

    def grad_psi_sq_phys():
        psih = to_spectral(psi_stack[0])
        u = to_physical(-1 * derivative.dy * psih)
        v = to_physical(+1 * derivative.dx * psih)
        return u * u + v * v

    def omega_grad(axis):
        qh = to_spectral(omega_stack[0])
        return to_physical((derivative.dx if axis == 'x' else derivative.dy) * qh)

    def N_now():
        N = -1.0 * J_phys(psi_stack[0], omega_stack[0], derivative)
        if F_phys is not None:
            N = N + F_phys
        return N

    def L_omega_phys(power):
        qh = to_spectral(omega_stack[0])
        return to_physical((L_hat ** power) * qh)

    def L_N_phys(power):
        Nh = to_spectral(N_now())
        return to_physical((L_hat ** power) * Nh)

    builders = {
        'omega_0':      lambda: grab_omega(0),
        'omega_m1':     lambda: grab_omega(1),
        'omega_m2':     lambda: grab_omega(2),
        'psi_0':        lambda: grab_psi(0),
        'psi_m1':       lambda: grab_psi(1),
        'psi_m2':       lambda: grab_psi(2),
        'grad_psi_sq':  grad_psi_sq_phys,
        'omega_x':      lambda: omega_grad('x'),
        'omega_y':      lambda: omega_grad('y'),
        'N_0':          N_now,
        'L_omega':      lambda: L_omega_phys(1),
        'L2_omega':     lambda: L_omega_phys(2),
        'L3_omega':     lambda: L_omega_phys(3),
        'L_N':          lambda: L_N_phys(1),
        'L2_N':         lambda: L_N_phys(2),
    }
    for field in input_fields:
        if field not in builders:
            raise ValueError(f"input field '{field}' not supported in rollout "
                             f"(if you need it, add a builder).")
        chans.append(builders[field]())
    # shape each (1, Ny, Nx) -> (1, C, Ny, Nx)
    x = torch.stack([c[0] for c in chans], dim=0)[None]
    return x.to(device=device, dtype=dtype)


# --------------------------------------------------------------------------- #
# Compute psi from omega (used for the psi stack)                             #
# --------------------------------------------------------------------------- #

def psi_from_omega(omega_phys, derivative):
    from qg.solver.opt.basis import to_spectral, to_physical
    psi_hat = derivative.inv_laplacian * to_spectral(omega_phys)
    return to_physical(psi_hat)


