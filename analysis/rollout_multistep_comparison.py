# Solver primitives (lifted from build_training_data_fixD.py to keep this     #
# script self-contained -- exact same operator definitions).                  #
# --------------------------------------------------------------------------- #

def J_phys(psi_phys, omega_phys, derivative):
    """Jacobian J(psi, omega) in physical space via spectral derivatives.

    2/3 dealiasing IS applied: after to_spectral(u*q) and to_spectral(v*q)
    we zero the modes above k_cut = (2/3)*k_nyquist via derivative.alias_mask.
    Without this, modes k1 + k2 > k_nyquist generated by the physical-space
    product fold back into the resolved spectrum (aliasing), and over O(10^6)
    timesteps this fills the high-k tail with broadband noise and drives a
    spurious cascade-like enstrophy growth. The production solver applies
    dealiasing at the assembled-RHS level via derivative.dealias() in
    qg.solver.opt._init_operator.Operator.RHS; we apply it inside the
    Jacobian instead, which is mathematically equivalent (the only non-linear
    operation in the QG RHS is this Jacobian).
    """
    from qg.solver.opt.basis import to_spectral, to_physical
    psih = to_spectral(psi_phys)
    qh   = to_spectral(omega_phys)
    uh = -1 * derivative.dy * psih
    vh = +1 * derivative.dx * psih
    u = to_physical(uh)
    v = to_physical(vh)
    q = to_physical(qh)
    uq_h = to_spectral(u * q)
    vq_h = to_spectral(v * q)
    # Apply 2/3 dealiasing to the nonlinear products before computing the
    # Jacobian.  We clone first because derivative.dealias() is in-place.
    uq_h = uq_h.clone()
    vq_h = vq_h.clone()
    derivative.dealias(uq_h)
    derivative.dealias(vq_h)
    j_hat = derivative.dx * uq_h + derivative.dy * vq_h
    return to_physical(j_hat)


def L_op(omega_phys, L_hat):
    from qg.solver.opt.basis import to_spectral, to_physical
    return to_physical(L_hat * to_spectral(omega_phys))


def build_L_hat(derivative, nu, mu, beta):
    L_hat = nu * derivative.laplacian - mu
    if beta != 0.0:
        L_hat = L_hat - beta * derivative.dx * derivative.inv_laplacian
    return L_hat


def ab2cn2_step_spectral(qh_n, qh_nm1, dt, derivative, L_hat, F_phys):
    """One AB2CN2 IMEX step -- byte-identical to build_training_data_fixD.py."""
    from qg.solver.opt.basis import to_spectral, to_physical

    def N_at_qh(qh):
        psi_hat = derivative.inv_laplacian * qh
        psi = to_physical(psi_hat)
        omega = to_physical(qh)
        N_phys = -1.0 * J_phys(psi, omega, derivative)
        if F_phys is not None:
            N_phys = N_phys + F_phys
        return to_spectral(N_phys)

    Nh_n   = N_at_qh(qh_n)
    Nh_nm1 = N_at_qh(qh_nm1)
    AB2_Nh = 1.5 * Nh_n - 0.5 * Nh_nm1
    rhs_hat   = qh_n + dt * (0.5 * L_hat * qh_n + AB2_Nh)
    denom_hat = 1.0 - 0.5 * dt * L_hat
    return rhs_hat / denom_hat


def rk4_step(omega, dt, derivative, L_hat, F_phys):
    def rhs(om):
        from qg.solver.opt.basis import to_spectral, to_physical
        psi_hat = derivative.inv_laplacian * to_spectral(om)
        psi = to_physical(psi_hat)
        N = -1.0 * J_phys(psi, om, derivative)
        if F_phys is not None:
            N = N + F_phys
        return L_op(om, L_hat) + N
    k1 = rhs(omega)
    k2 = rhs(omega + 0.5 * dt * k1)
    k3 = rhs(omega + 0.5 * dt * k2)
    k4 = rhs(omega + dt * k3)
    return omega + (dt / 6.0) * (k1 + 2 * k2 + 2 * k3 + k4)


# --------------------------------------------------------------------------- #
# E_anal: (1/12)[L^3 omega + L^2 N] -- needed at every coarse step            #
# --------------------------------------------------------------------------- #

def E_analytical_phys(omega_phys, derivative, L_hat, F_phys):
    """Analytical part of the closure bracket: (1/12) * [L^3 omega + L^2 N]."""
    from qg.solver.opt.basis import to_spectral, to_physical
    qh = to_spectral(omega_phys)
    L3_omega = to_physical(L_hat ** 3 * qh)

    psi_hat = derivative.inv_laplacian * qh
    psi = to_physical(psi_hat)
    N = -1.0 * J_phys(psi, omega_phys, derivative)
    if F_phys is not None:
        N = N + F_phys
    Nh = to_spectral(N)
    L2_N = to_physical(L_hat ** 2 * Nh)
    return (1.0 / 12.0) * (L3_omega + L2_N)


# --------------------------------------------------------------------------- #
# Spectra -- CORRECTED radial sum (not radial mean)                           #
# --------------------------------------------------------------------------- #

def radial_sum_spectrum(field2d, Lx, Ly):
    """
    Return (k_centers, P(k)) where
        P(k) dk = sum over k-shell of |f_hat(k)|^2

    Uses physical wavenumbers (kx = 2*pi*n/Lx), and *sums* within each annulus.
    Bin width dk is chosen as the larger of (2*pi/Lx, 2*pi/Ly) so each bin
    contains at least one grid mode.

    The previous visualization used:
        - integer-index kx (only correct for L = 2*pi)
        - mean within annulus (divides out the k-density-of-states factor)
      which is why the previous E(k), Z(k) plots looked flat instead of
      showing the canonical k^{-3} forward-enstrophy-cascade slope.
    """
    Ny, Nx = field2d.shape
    # Physical wavenumbers
    kx = np.fft.fftfreq(Nx, d=Lx / Nx) * 2.0 * np.pi   # shape (Nx,)
    ky = np.fft.fftfreq(Ny, d=Ly / Ny) * 2.0 * np.pi
    KX, KY = np.meshgrid(kx, ky, indexing='xy')
    Kmag = np.sqrt(KX ** 2 + KY ** 2)
    # Unitary FFT normalization: Parseval gives sum |f_hat|^2 / (Nx*Ny) = mean |f|^2
    fhat = np.fft.fft2(field2d) / (Nx * Ny)
    psd  = np.abs(fhat) ** 2

    dk = max(2.0 * np.pi / Lx, 2.0 * np.pi / Ly)
    kmax = float(Kmag.max())
    bins = np.arange(0.0, kmax + dk, dk)
    sp, _ = np.histogram(Kmag.ravel(), bins=bins, weights=psd.ravel())  # SUM
    kc = 0.5 * (bins[:-1] + bins[1:])
    # P(k) so that sum P(k) * dk = total variance:
    return kc, sp / dk


def enstrophy_spectrum(omega_phys, Lx, Ly):
    """Z(k) = 0.5 * sum_{|k|=k} |omega_hat(k)|^2 / dk."""
    kc, sp = radial_sum_spectrum(omega_phys, Lx, Ly)
    return kc, 0.5 * sp


def energy_spectrum(omega_phys, Lx, Ly):
    """E(k) = 0.5 * k^{-2} * sum_{|k|=k} |omega_hat(k)|^2 / dk.

    Uses omega = -lap psi  =>  |omega_hat|^2 = k^4 |psi_hat|^2,
    so KE density 0.5 |u_hat|^2 = 0.5 k^2 |psi_hat|^2 = 0.5 k^{-2}|omega_hat|^2.
    """
    kc, sp = radial_sum_spectrum(omega_phys, Lx, Ly)
    k_safe = np.where(kc <= 0, np.inf, kc)
    return kc, 0.5 * sp / (k_safe ** 2)


